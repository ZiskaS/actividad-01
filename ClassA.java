public class ClassA {
    private String foo;
    private String bar;

    // Constructor
    public ClassA(String foo, String bar) {
        this.foo = foo;
        this.bar = bar;
    }

    // Getters and setters
    public String getFoo() {
        return foo;
    }

    public void setFoo(String foo) {
        this.foo = foo;
    }

    public String getBar() {
        return bar;
    }

    public void setBar(String bar) {
        this.bar = bar;
    }
}

